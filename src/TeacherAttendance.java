    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
String name = jTextField1.getText().trim();   // Teacher Name
        String branch = jTextField2.getText().trim(); // Teacher Branch

        if(name.isEmpty() || branch.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(null, "Please enter Name and Branch");
            return;
        }

        try {
            Connection conn = DBConnection.connect(); // Connect to DB
            String sql = "SELECT attendance FROM teachers WHERE name=? AND branch=?";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, name);
            pst.setString(2, branch);
            ResultSet rs = pst.executeQuery();

            if(rs.next()) {
                // Display attendance in the text field
                jTextField3.setText(rs.getString("attendance"));
            } else {
                javax.swing.JOptionPane.showMessageDialog(null, "No record found for this teacher");
                jTextField3.setText(""); // Clear field if no record
            }

            rs.close();
            pst.close();
            conn.close();
        } catch(Exception e) {
            javax.swing.JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    } 
