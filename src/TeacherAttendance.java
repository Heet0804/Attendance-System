    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try {
        Connection conn = DBConnection.connect(); // Connect to DB
        String sql = "SELECT name, roll_number, attendance FROM students WHERE attendance < 75";
        PreparedStatement pst = conn.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();

        StringBuilder defaultersList = new StringBuilder();
        while(rs.next()) {
            String name = rs.getString("name");
            String roll = rs.getString("roll_number");
            String attendance = rs.getString("attendance");
            defaultersList.append("Name: ").append(name)
                          .append(", Roll No: ").append(roll)
                          .append(", Attendance: ").append(attendance)
                          .append("\n");
        }

        if(defaultersList.length() == 0) {
            JOptionPane.showMessageDialog(this, "No student defaulters found!");
        } else {
            JOptionPane.showMessageDialog(this, defaultersList.toString(), "Student Defaulters", JOptionPane.WARNING_MESSAGE);
        }

        rs.close();
        pst.close();
        conn.close();
    } catch(Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_jButton3ActionPerformed

