    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
    String email = jTextField2.getText();
    String password = jTextField3.getText();

    // Check if a role is selected
    boolean roleSelected = jRadioButton1.isSelected() || jRadioButton2.isSelected();

    // Check if a course is selected
    boolean courseSelected = jRadioButton3.isSelected() || jRadioButton4.isSelected();

    // Email validation
    if (email.isEmpty() || email.length() < 8 || !email.contains("@") || !email.contains(".")) {
        javax.swing.JOptionPane.showMessageDialog(this, "Email must be at least 8 characters and contain '@' and '.'");
        return;
    }

    // Password validation using regex
    String passwordPattern = "^(?=.*[a-z])(?=.*[A-Z])(?=.*\\d)(?=.*[@$!%*?&])[A-Za-z\\d@$!%*?&]{1,}$";
    if (!password.matches(passwordPattern)) {
        javax.swing.JOptionPane.showMessageDialog(this, "Password must contain:\n- At least 1 uppercase letter\n- At least 1 lowercase letter\n- At least 1 number\n- At least 1 special character");
        return;
    }

    // Check role and course selection
    if (!roleSelected) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please select a role (Student or Teacher).");
        return;
    }

    if (!courseSelected) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please select a course.");
        return;
    }

    // All inputs valid â†’ Login successful
    javax.swing.JOptionPane.showMessageDialog(this, "Login Successful!");

    // Open respective page based on role
    if (jRadioButton1.isSelected()) { // Student
        StudentAttendance sa = new StudentAttendance(); // JFrame class for student page
        sa.setVisible(true);
        this.dispose(); // Close login page
    } else if (jRadioButton2.isSelected()) { // Teacher
        TeacherAttendance ta = new TeacherAttendance(); // JFrame class for teacher page
        ta.setVisible(true);
        this.dispose(); // Close login page
    }
// TODO add your handling code here:
    }//GEN-LAST:event_jButton3ActionPerformed

